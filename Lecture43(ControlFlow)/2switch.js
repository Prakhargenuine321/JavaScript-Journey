const month = "march";

switch (month) {
    case "jan":
        console.log("January");
        break; //it breaks the control flow of an program.
    case "feb":
        console.log("feb");
        break;
    case "march":
        console.log("march");
        break; 
    case "april":
        console.log("april");
        break;

    default:
        console.log("default case match");
        break;
}


